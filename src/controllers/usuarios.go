// AtualizarSenha permite alterar a senha do usuário
func AtualizarSenha(w http.ResponseWriter, r *http.Request) {
	usuarioIDTNoToken, erro := autenticacao.ExtrairUsuarioID(r)
	if erro != nil {
		respostas.Erro(w, http.StatusUnauthorized, erro)
		return
	}

	parametros := mux.Vars(r)
	usuarioID, erro := strconv.ParseUint(parametros["usuarioId"], 10, 64)
	if erro != nil {
		respostas.Erro(w, http.StatusBadRequest, erro)
		return

	}

	if usuarioIDTNoToken != usuarioID {
		respostas.Erro(w, http.StatusForbidden, errors.New("Não é possível atualizar a senha de outro usuário"))
		return
	}

	corpoRequisicao, erro := ioutil.ReadAll(r.Body)

	var senha model.Senha
	if erro = json.Unmarshal(corpoRequisicao, &senha); erro != nil{
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}

	db, erro := banco.Conectar()
	if erro != nil {
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}
	defer db.Close()

	repositorio := repository.NovoRepositorioDeUsuarios(db)
	senhaSalvaNoBanco, erro := repositorio.BuscarSenhaDoUsuarioPeloID(usuarioID)
	if erro != nil{
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}

	if erro = security.VerificarSenha(senhaSalvaNoBanco, senha.Atual); erro != nil{
		respostas.Erro(w, http.StatusUnauthorized, errors.New("A senha atual não condiz com a senha que está no banco"))
		return
	}

	senhaComHash, erro := security.Hash(senha.Nova)
	if erro != nil{
		respostas.Erro(w, http.StatusBadRequest, erro)
		return
	}

	if erro = repositorio.AtualizarSenha(usuarioID, string(senhaComHash)); erro != nil{
		respostas.Erro(w, http.StatusInternalServerError, erro)
		return
	}

	respostas.JSON(w, http.StatusNoContent, nil)


}

